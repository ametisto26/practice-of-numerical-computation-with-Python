# -*- coding: utf-8 -*-
"""
複素関数
"""
import cmath


a = 1.2 + 3.4j


print('sqrt(a) = ', cmath.sqrt(a))

print('exp(a) = ', cmath.exp(a))

print('sin(a) = ', cmath.sin(a))

print('tan(a) = ', cmath.tan(a))

print('log(a) = ', cmath.log(a))

print('log10(a) = ', cmath.log10(a))

print('atan(a) = ', cmath.atan(a))


