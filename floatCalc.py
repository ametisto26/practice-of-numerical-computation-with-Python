"""
math module
"""
import math

a = input('非負の数 a = ')
b = input('正の数 b = ')
a, b = float(a), float(b)

print('a + b = ', a + b)
print('a - b = ', a - b)
print('a * b = ', a * b)
print('a / b = ', a / b)


print('sqrt(a) = ', math.sqrt(a))
print('sqrt(b) = ', math.sqrt(b))


print('exp(a) = ', math.exp(a))

print('sin(a) = ', math.sin(a))

print('tan(a) = ', math.tan(a))

print('log(a) = ', math.log(a))

print('log10(a) = ', math.log10(a))

print('atan(a) = ', math.atan(a))
